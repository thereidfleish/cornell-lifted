import SwiftUI

extension Font {
    static func tenorSans(size: CGFloat) -> Font {
        return .custom("TenorSans", size: size)
    }
    
    static func schoolbell(size: CGFloat) -> Font {
        return .custom("Schoolbell-Regular", size: size)
    }
}
